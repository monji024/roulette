def type_out(text, delay)
  text.each_char do |ch|
    print ch
    $stdout.flush
    sleep(delay) unless ch == ' '
  end
  puts
end
def glitch_out(text)
  noise_chars = %w[# % & $ ~ ^ * ¬§ ‚Ėď ‚Ėí ‚ĖĎ]
  2.times do
    scrambled = text.chars.map { |c| c == ' ' ? ' ' : noise_chars.sample }.join
    print "\r#{scrambled}"
    $stdout.flush
    sleep(0.04)
  end
  print "\r#{' ' * text.length}\r"
  type_out(text, 0.012)
end

def print_ascii(name)
  key = name.to_s.strip.upcase
  art = BANNERS[key]
  if art.nil?
    warn "effects.rb: unknown banner '#{name}'"
    exit 1
  end
  puts art
end

def main
  command = ARGV[0]
  case command
  when 'type'
    text = ARGV[1]
    delay = (ARGV[2] || '0.02').to_f
    if text.nil?
      warn 'effects.rb: usage: ruby effects.rb type "<text>" [delay]'
      exit 1
    end
    type_out(text, delay)
  when 'ascii'
    name = ARGV[1]
    if name.nil?
      warn 'effects.rb: usage: ruby effects.rb ascii <BANNER_NAME>'
      exit 1
    end
    print_ascii(name)
  when 'glitch'
    text = ARGV[1]
    if text.nil?
      warn 'effects.rb: usage: ruby effects.rb glitch "<text>"'
      exit 1
    end
    glitch_out(text)
  else
    warn 'effects.rb: usage: ruby effects.rb <type|ascii|glitch> ...'
    exit 1
  end
end

main if __FILE__ == $PROGRAM_NAME
